from google_play_scraper import reviews_all, Sort
import pandas as pd

def scrape_traveloka_reviews():
    scrap_data = reviews_all(
        app_id='com.traveloka.android',
        lang='id',
        country='id',
        sort=Sort.NEWEST
    )

    raw_df = pd.DataFrame(scrap_data)

    raw_df.to_csv(
        'traveloka_raw_reviews.csv',
        index=False,
        encoding='utf-8'
    )

    print(f'Jumlah data yang didapat: {len(raw_df)}')

if __name__ == "__main__":
    scrape_traveloka_reviews()